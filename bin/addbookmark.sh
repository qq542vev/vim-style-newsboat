        $1*) OPTARG="$OPTARG --bottom"
      esac
      case '--no-bottom' in
        "$1") OPTARG=; break ;;
        $1*) OPTARG="$OPTARG --no-bottom"
      esac
      case '--duplicate' in
        "$1") OPTARG=; break ;;
        $1*) OPTARG="$OPTARG --duplicate"
      esac
      case '--no-duplicate' in
        "$1") OPTARG=; break ;;
        $1*) OPTARG="$OPTARG --no-duplicate"
      esac
      case '--file' in
        "$1") OPTARG=; break ;;
        $1*) OPTARG="$OPTARG --file"
      esac
