bottomFlag='0'
duplicateFlag='0'
bookmarkFile="${HOME}/bookmark.html"
purgeFlag='0'
removeFlag='0'
itemTemplate='<li><a rel="noreferrer" href="${uri}">${title}</a>${description:+"<p>${description}</p>"}</li>'
REST=''
